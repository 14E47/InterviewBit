class Solution:
    # @param A : list of integers
    # @return an integer
    def firstMissingPositive(self, A):

        A.sort()
        A = [i for i in A if i > 0]

        n = len(A)
        if n == 0:
            return 1
        else:
            for i in range(1,n+1):
                if i != A[i-1]:
                    return i
            return n+1

