class Solution:
    # @param A : list of integers
    # @return a list of integers
    def solve(self, A):
        n = len(A)
        c = A.count(0)
        if c == 0:
            return A

        if A == [0]*n:
            return A

        non_zero = 0
        for i,v in enumerate(A):
            if v != 0:
                A[non_zero] = v
                if non_zero != i:
                    A[i] = 0
                non_zero += 1


        return A

